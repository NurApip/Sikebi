    public final void getData() {
        DefaultTableModel dtm = (DefaultTableModel) tableHewan.getModel();

        dtm.setRowCount(0);

        List<Hewan> listHewan = conHewan.showHewan();
        String[] data = new String[4];
        for (Hewan newHewan : listHewan) {
            data[0] = Integer.toString(newHewan.getId());
            data[1] = newHewan.getNama();
            data[2] = newHewan.getSpesies();
            data[3] = Integer.toString(newHewan.getUmur());
            dtm.addRow(data);
        }
    }
    

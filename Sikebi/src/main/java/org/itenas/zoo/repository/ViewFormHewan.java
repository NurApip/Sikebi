    private void btn_upload_imageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_upload_imageActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser(); 
    String namaFileMobil = null; 
    ImageIcon mobilicon = null; 
    
    int returnValue = fileChooser.showOpenDialog(null); 
    
    if (returnValue == JFileChooser.APPROVE_OPTION) { 
        File file = fileChooser.getSelectedFile(); 
        namaFileMobil = file.getAbsolutePath(); 
        txt_image_path.setText(namaFileMobil); 
        
        try {
            File image = new File(namaFileMobil); 
            BufferedImage thumbnail = Thumbnails.of(image) 
                                              .size(180, 220)
                                              .asBufferedImage();
            
            
            ByteArrayOutputStream os = new ByteArrayOutputStream();
            ImageIO.write(thumbnail, "jpeg", os); 
            InputStream is = new ByteArrayInputStream(os.toByteArray()); 
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            
            for (int readNum; (readNum = is.read(buf)) != -1;) { 
                bos.write(buf, 0, readNum); 
            }
           
            mobilicon = new ImageIcon(thumbnail); 
            lbl_image.setIcon(mobilicon);
            
            hewanImage = bos.toByteArray(); 
            
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error processing image: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "No file selected.");}
    }//GEN-LAST:event_btn_upload_imageActionPerformed


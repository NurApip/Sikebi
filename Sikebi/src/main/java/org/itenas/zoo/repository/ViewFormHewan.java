    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        int i = tableHewan.getSelectedRow();

        if (i == -1) {
            JOptionPane.showMessageDialog(btnupdate, "Harap pilih salah satu data", "warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int idMobil = Integer.parseInt(tableHewan.getModel().getValueAt(i, 0).toString());
        String oldNama = tableHewan.getModel().getValueAt(i, 1).toString();

        conHewan.updateHewan(txtnamahewan.getText(), oldNama , Integer.parseInt(txtumur.getText()), idMobil);
        JOptionPane.showMessageDialog(btnupdate, "Data berhasil diUpdate");
        getData();
        clearData();
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int i =tableHewan.getSelectedRow();

    if (i == -1) {
        JOptionPane.showMessageDialog(btndelete, "Harap pilih salah satu data", "warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(btndelete, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        
        int idHewan = Integer.parseInt(tableHewan.getModel().getValueAt(i, 0).toString());

        boolean isDeleted = conHewan.deleteHewan(idHewan);

        if (isDeleted) {
            JOptionPane.showMessageDialog(btndelete, "Data berhasil dihapus.");
            getData(); 
            clearData(); 
        } else {
            JOptionPane.showMessageDialog(btndelete, "Data gagal dihapus.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }          
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        // TODO add your handling code here:
        hasil = conHewan.insertHewan(txtnamahewan.getText(),txtspesies.getText(), Integer.parseInt(txtumur.getText()));
        if (hasil) {
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
            getData();
            clearData();
        } else {
            JOptionPane.showMessageDialog(null, "Data gagal ditambahkan", " Pesan", JOptionPane.ERROR_MESSAGE);
        }
        
        String targetDirPath = "src/main/java/org/stenas/copprektskum/jdbc/images";
        File targetDir = new File(targetDirPath);

        if (!targetDir.exists()) {
            targetDir.mkdirs();
        }

        String fileName = txtnamahewan.getText().replaceAll("\\s+", "_") + ".Jpeg";
        File targetFile = new File(targetDir, fileName);

        try (OutputStream os = new FileOutputStream(targetFile)) {
            os.write(hewanImage); 
            JOptionPane.showMessageDialog(null, "File saved to: " + targetFile.getAbsolutePath());
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error saving file: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnsubmitActionPerformed


    private void tableHewanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableHewanMouseClicked
        // TODO add your handling code here:
        int i = tableHewan.getSelectedRow();
        
        TableModel model = tableHewan.getModel();
        String namaHewan = model.getValueAt(i, 1).toString();
        String spesies = model.getValueAt(i, 1).toString();
        int umur = Integer.parseInt(model.getValueAt(i, 2).toString());
        txtnamahewan.setText(tableHewan.getValueAt(i, 1).toString());
        txtspesies.setText(tableHewan.getValueAt(i, 2).toString());
        txtumur.setText(tableHewan.getValueAt(i, 3).toString());
        
    try{
        File imageFile = new File("src/main/java/org/itenas/ooppraktikum/jdbc/images/" + namaHewan + ".jpeg");

        if (imageFile.exists()) {
            // Membaca file gambar
            BufferedImage originalImage = ImageIO.read(imageFile);

            // Mengubah ukuran gambar
            BufferedImage resizedImage = Thumbnails.of(originalImage)
                .size(230, 130)
                .asBufferedImage();

            // Menampilkan gambar pada JLabel
            ImageIcon icon = new ImageIcon(resizedImage);
            lbl_image.setIcon(icon);

            // (Opsional) Simpan gambar yang di-resize sebagai byte array
            ByteArrayOutputStream baos = new ByteArrayOutputStream();
            ImageIO.write(resizedImage, "jpeg", baos);
            byte[] imageBytes = baos.toByteArray();

            // (Opsional) Lakukan sesuatu dengan byte array imageBytes jika diperlukan
        } else {
            // Jika file gambar tidak ditemukan
            lbl_image.setIcon(null);
            JOptionPane.showMessageDialog(null, "Gambar tidak ditemukan untuk: " + namaHewan);
        }
    } catch (IOException e) {
        // Penanganan jika terjadi kesalahan saat membaca gambar
        lbl_image.setIcon(null);
        JOptionPane.showMessageDialog(null, "Error saat memuat gambar: " + e.getMessage());
    }
    }//GEN-LAST:event_tableHewanMouseClicked


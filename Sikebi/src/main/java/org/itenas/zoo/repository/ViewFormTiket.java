    private void button_createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_createActionPerformed
        // TODO add your handling code here:
        try {
        // Validasi input data
        if (text_id.getText().trim().isEmpty()
            || text_namaAcara.getText().trim().isEmpty()
            || text_Harga_Tiket.getText().trim().isEmpty()
            || text_tanggal.getText().trim().isEmpty()
            || combo_kategori_tiket.getSelectedItem() == null
            || text_zona_tiket.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Parsing input data dengan aman
        String id = text_id.getText().trim();
        String namaAcara = text_namaAcara.getText().trim();
        double hargaTiket = Double.parseDouble(text_Harga_Tiket.getText().trim());
        String tanggalAcara = text_tanggal.getText().trim();
        String kategoriTiket = combo_kategori_tiket.getSelectedItem().toString();
        String zonaTiket = text_zona_tiket.getText().trim();

        // Set data ke objek tiket (contoh data simulasi)
        tiket = new Tiket();
        tiket.setId(id); // Mengatur ID Tiket
        tiket.setNamaAcara(namaAcara); // Mengatur Nama Acara
        tiket.setHarga(hargaTiket); // Harga Tiket disesuaikan dengan variabel gajiPokok
        tiket.setTanggal(tanggalAcara); // Tanggal Acara (diasumsikan sebagai String)
        tiket.setKategori(kategoriTiket); // Mengatur Kategori Tiket
        tiket.setZona(zonaTiket); // Zona Tiket

        // Simpan data
        tiketCrudService.create(tiket);
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan!", "Success", JOptionPane.INFORMATION_MESSAGE);

        // Refresh tabel dan kosongkan input field
        loadData();
        emptyField();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Format angka tidak valid! Periksa input Harga Tiket atau Zona Tiket.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_button_createActionPerformed

    private void button_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_UpdateActionPerformed
        // Inisialisasi objek dan service yang diperlukan
        tiketCrudService = new TiketCrudServiceDbImpl(new ConnectionManager());
        
        try {
            // Validasi input data
            if (text_id.getText().trim().isEmpty()
                    || text_namaAcara.getText().trim().isEmpty()
                    || text_Harga_Tiket.getText().trim().isEmpty()
                    || text_tanggal.getText().trim().isEmpty()
                    || combo_kategori_tiket.getSelectedItem() == null
                    || text_zona_tiket.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Ambil data dari input form
            String id = text_id.getText().trim();
            String namaAcara = text_namaAcara.getText().trim();
            String hargaTiketStr = text_Harga_Tiket.getText().replace(",", "");
            double hargaTiket = Double.parseDouble(hargaTiketStr.trim());
            String tanggalAcara = text_tanggal.getText().trim();
            String kategoriTiket = combo_kategori_tiket.getSelectedItem().toString();
            String zonaTiket = text_zona_tiket.getText().trim();

            // Set data ke objek tiket
            Tiket tiket = new Tiket();
            tiket.setId(id);
            tiket.setNamaAcara(namaAcara);
            tiket.setHarga(hargaTiket);
            tiket.setTanggal(tanggalAcara);
            tiket.setKategori(kategoriTiket);
            tiket.setZona(zonaTiket);

            // Lakukan update data
            tiketCrudService.update(id, tiket);
            JOptionPane.showMessageDialog(this, "Data Tiket berhasil diperbarui!", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadData();
            emptyField();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Format angka tidak valid! Periksa kembali input Harga Tiket.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_button_UpdateActionPerformed

    private void button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_deleteActionPerformed
        // Inisialisasi service
        tiketCrudService = new TiketCrudServiceDbImpl(new ConnectionManager());

        // Ambil NIP dari input
        String kode = text_id.getText().trim();

        // Validasi apakah NIP kosong
        if (kode.isEmpty()) {
            JOptionPane.showMessageDialog(this, "kode harus diisi sebelum menghapus data!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Konfirmasi penghapusan data
        int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure to delete it?", "Warning", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            // Hapus data dan cek keberhasilannya
            boolean isDeleted = tiketCrudService.delete(kode);
            if (isDeleted) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Success", JOptionPane.INFORMATION_MESSAGE);
                loadData(); // Refresh tabel
                emptyField(); // Kosongkan input field
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data. Periksa kembali kode!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_button_deleteActionPerformed


    private void button_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_searchActionPerformed
         String id = text_search.getText().trim();

    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "ID tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Inisialisasi service
    CrudService CrudService = new TiketCrudServiceDbImpl(new ConnectionManager());
    T tiket = tiketCrudService.findOne(id);

    if (tiket != null) {
        // Tampilkan data ke tabel
        DefaultTableModel tableModel = (DefaultTableModel) tabel_kelola_tiket.getModel();
        tableModel.setRowCount(0); // Hapus data lama

        Object[] rowData = {
            tiket.getId(),
            tiket.getNamaAcara(),
            df.format(tiket.getHarga()),
            tiket.getTanggal(),
            tiket.getKategori(),
            tiket.getZona()
        };
        tableModel.addRow(rowData);
    } else {
        JOptionPane.showMessageDialog(this, "Data tidak ditemukan!", "Error", JOptionPane.INFORMATION_MESSAGE);
        // Hapus data di tabel jika data tidak ditemukan
        DefaultTableModel tableModel = (DefaultTableModel) tabel_kelola_tiket.getModel();
        tableModel.setRowCount(0);
    }
    }//GEN-LAST:event_button_searchActionPerformed


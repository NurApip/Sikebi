    private void loadData() {
    tiketCrudService = new TiketCrudServiceDbImpl(new ConnectionManager());
    List<T> listTiket = tiketCrudService.findAll(); // Mengambil semua data tiket
    Object[][] objectTiket = new Object[listTiket.size()][6];

    int counter = 0;

    for (T tiket : listTiket) {
        objectTiket[counter][0] = tiket.getId();
        objectTiket[counter][1] = tiket.getNamaAcara();
        objectTiket[counter][2] = df.format(tiket.getHarga());
        objectTiket[counter][3] = tiket.getTanggal();
        objectTiket[counter][4] = tiket.getKategori();
        objectTiket[counter][5] = tiket.getZona();
        counter++;
    }

    tabel_kelola_tiket.setModel(new javax.swing.table.DefaultTableModel(
        objectTiket,
        new String[]{
            "ID", "Nama Acara", "Harga Tiket", "Tanggal", "Kategori", "Zona"
        }
    ));
}


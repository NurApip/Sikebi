    private void tabel_kelola_tiketMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_kelola_tiketMouseClicked
        String id, nama_acara, kategori_tiket;
        double hargatiket, hibahPenelitian;
        int pengalaman;

        int row = tabel_kelola_tiket.getSelectedRow();
        String gajiPokokStr = tabel_kelola_tiket.getValueAt(row, 2).toString().replace(",", "");
        String hibahPenelitianStr = tabel_kelola_tiket.getValueAt(row, 5).toString().replace(",", "");

        id = tabel_kelola_tiket.getValueAt(row, 0).toString();
        nama_acara = tabel_kelola_tiket.getValueAt(row, 1).toString();
        hargatiket = Double.parseDouble(gajiPokokStr);
        pengalaman = Integer.parseInt(tabel_kelola_tiket.getValueAt(row, 3).toString());
        kategori_tiket = tabel_kelola_tiket.getValueAt(row, 4).toString();
        hibahPenelitian = Double.parseDouble(hibahPenelitianStr);

        text_id.setText(id);
        text_id.setEnabled(false);
        text_namaAcara.setText(nama_acara);
        text_Harga_Tiket.setText(String.valueOf(df.format(hargatiket)));
        text_tanggal.setText(String.valueOf(pengalaman));
        combo_kategori_tiket.setSelectedItem(bidangKeahlian);
        text_zona_tiket.setText(String.valueOf(df.format(hibahPenelitian)));
    }//GEN-LAST:event_tabel_kelola_tiketMouseClicked


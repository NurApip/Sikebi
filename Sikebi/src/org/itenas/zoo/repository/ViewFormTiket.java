    private void button_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_searchActionPerformed
        String nip = text_search.getText().trim();

        if (nip.isEmpty()) {
            JOptionPane.showMessageDialog(this, "NIP tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        dosenTetapCrudService = new DosenTetapCrudServiceDBImpl(new ConnectionManager());
        DosenTetap dosenTetap = dosenTetapCrudService.findOne(nip);

        if (dosenTetap != null) {
            // Tampilkan data ke tabel
            DefaultTableModel tableModel = (DefaultTableModel) tabel_kelola_tiket.getModel();
            tableModel.setRowCount(0); // Hapus data lama

            Object[] rowData = {
                dosenTetap.getNip(),
                dosenTetap.getNama(),
                df.format(dosenTetap.getGajiPokok()),
                dosenTetap.getPengalaman(),
                dosenTetap.getBidangKeahlian(),
                df.format(dosenTetap.getNominalHibahPenelitian()),
                dosenTetap.hitungTotalTunjangan(),
                df.format(dosenTetap.hitungTotalGaji())
            };
            tableModel.addRow(rowData);
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan!", "Error", JOptionPane.INFORMATION_MESSAGE);
            // Hapus data di tabel jika data tidak ditemukan
            DefaultTableModel tableModel = (DefaultTableModel) tabel_kelola_tiket.getModel();
            tableModel.setRowCount(0);
        }
    }//GEN-LAST:event_button_searchActionPerformed

